import tkinter as tk
import random

ВАРИАНТЫ = ["Камень", "Ножницы", "Бумага"]
ЭМОДЗИ = {"Камень": "🪨", "Ножницы": "✂️", "Бумага": "📄"}

счёт_игрок = 0
счёт_компьютер = 0
счёт_ничья = 0


def определить_победителя(игрок, компьютер):
    # Определяет результат раунда
    if игрок == компьютер:
        return "ничья"
    победные_комбинации = {"Камень": "Ножницы", "Ножницы": "Бумага", "Бумага": "Камень"}
    if победные_комбинации[игрок] == компьютер:
        return "игрок"
    return "компьютер"


def сыграть(выбор_игрока):
    global счёт_игрок, счёт_компьютер, счёт_ничья
    выбор_компьютера = random.choice(ВАРИАНТЫ)
    результат = определить_победителя(выбор_игрока, выбор_компьютера)

    if результат == "игрок":
        счёт_игрок += 1
        текст_результата = "Вы выиграли! 🎉"
        цвет = "#2e7d32"
    elif результат == "компьютер":
        счёт_компьютер += 1
        текст_результата = "Компьютер выиграл! 💻"
        цвет = "#c62828"
    else:
        счёт_ничья += 1
        текст_результата = "Ничья! 🤝"
        цвет = "#f9a825"

    метка_игрок.config(text=f"Вы: {ЭМОДЗИ[выбор_игрока]} {выбор_игрока}")
    метка_компьютер.config(text=f"Компьютер: {ЭМОДЗИ[выбор_компьютера]} {выбор_компьютера}")
    метка_результат.config(text=текст_результата, fg=цвет)
    метка_счёт.config(text=f"Счёт — Вы: {счёт_игрок}   Компьютер: {счёт_компьютер}   Ничьи: {счёт_ничья}")


def сбросить_счёт():
    global счёт_игрок, счёт_компьютер, счёт_ничья
    счёт_игрок = 0
    счёт_компьютер = 0
    счёт_ничья = 0
    метка_счёт.config(text="Счёт — Вы: 0   Компьютер: 0   Ничьи: 0")
    метка_результат.config(text="Сделайте свой выбор!", fg="#333333")
    метка_игрок.config(text="Вы: —")
    метка_компьютер.config(text="Компьютер: —")


окно = tk.Tk()
окно.title("Камень Ножницы Бумага")
окно.geometry("400x450")
окно.resizable(False, False)
окно.config(bg="#e3f2fd")

заголовок = tk.Label(окно, text="Камень Ножницы Бумага", font=("Arial", 18, "bold"), bg="#e3f2fd", fg="#1565c0")
заголовок.pack(pady=15)

метка_игрок = tk.Label(окно, text="Вы: —", font=("Arial", 13), bg="#e3f2fd")
метка_игрок.pack(pady=5)

метка_компьютер = tk.Label(окно, text="Компьютер: —", font=("Arial", 13), bg="#e3f2fd")
метка_компьютер.pack(pady=5)

метка_результат = tk.Label(окно, text="Сделайте свой выбор!", font=("Arial", 14, "bold"), bg="#e3f2fd", fg="#333333")
метка_результат.pack(pady=15)

рамка_кнопок = tk.Frame(окно, bg="#e3f2fd")
рамка_кнопок.pack(pady=10)

for вариант in ВАРИАНТЫ:
    кнопка = tk.Button(
        рамка_кнопок,
        text=f"{ЭМОДЗИ[вариант]}\n{вариант}",
        font=("Arial", 12, "bold"),
        width=8, height=3,
        bg="#1976d2", fg="white",
        activebackground="#1565c0",
        command=lambda в=вариант: сыграть(в)
    )
    кнопка.pack(side="left", padx=8)

метка_счёт = tk.Label(окно, text="Счёт — Вы: 0   Компьютер: 0   Ничьи: 0", font=("Arial", 11), bg="#e3f2fd", fg="#555555")
метка_счёт.pack(pady=20)

кнопка_сброс = tk.Button(окно, text="Сбросить счёт", font=("Arial", 11, "bold"), bg="#e53935", fg="white", activebackground="#b71c1c", command=сбросить_счёт)
кнопка_сброс.pack(pady=5)

окно.mainloop()